#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		sg;
	long	nb;

	sg = 0;
	nb = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-' )
	{
		if (str[i] == '-')
			sg = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	if (sg == 1)
		return (nb * -1);
	return (nb);
}
