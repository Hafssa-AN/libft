#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (fd < 0)
		return ;
	if (n < -2147483648 && n > 2147483647)
		return ;
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
	}
	else if (n >= 0 && n <= 9)
		ft_putchar_fd(48 + n, fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n * -1, fd);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
